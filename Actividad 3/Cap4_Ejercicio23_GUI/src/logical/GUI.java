    private void buttonSolucionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSolucionActionPerformed
        //Creacion de clase para mostrar dos cifras decimas
        DecimalFormat df = new DecimalFormat("#.##");        
        //Leemos los valores por las bandejas de textos
        int A = Integer.parseInt(textA.getText());
        int B = Integer.parseInt(textB.getText());
        int C = Integer.parseInt(textC.getText());
        //Creamos el objeto
        Ecuacion2Grado ecuacion_parametros = new Ecuacion2Grado(A,B,C);
        //Mostramos el resultado por la bandeja respectiva de texto
        String mensaje;
        switch(ecuacion_parametros.solucionEcuacion()){
            case 0:
            {   mensaje = "Primera solucion: " + df.format(ecuacion_parametros.realPart1) + " + " + df.format(ecuacion_parametros.imgPart1) + " i\n";
                mensaje = mensaje + "Segunda solucion: " + df.format(ecuacion_parametros.realPart2) + " + " + df.format(ecuacion_parametros.imgPart2) + " i\n";
            break;
            }
            case 1:
            {
                mensaje = "Primera solucion: " + df.format(ecuacion_parametros.realPart1)+ " \n";
                mensaje = mensaje + "Segunda solucion: " + df.format(ecuacion_parametros.realPart2)+ " \n";
                break;
            }
            default: { throw new AssertionError(); }
        }
        textareaSoluciones.setText(mensaje);
    }//GEN-LAST:event_buttonSolucionActionPerformed

